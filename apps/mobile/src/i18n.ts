export type DictionaryKey = keyof typeof dictionaries.en;

export function t(locale: Locale, key: DictionaryKey, vars?: Record<string, string | number>) {
  const template = dictionaries[locale][key] ?? dictionaries.en[key];
  if (!vars) return template;
  return Object.keys(vars).reduce((acc, variable) => {
    return acc.replace(new RegExp(`{${variable}}`, "g"), String(vars[variable]));
  }, template);
}
